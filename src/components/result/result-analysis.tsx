import { Card } from "@/components/ui/card";

type Analysis = {
  summary?: string;
  strengths?: string[];
  weaknesses?: string[];
  recommendations?: string[];
  progress?: { summary?: string; improved?: string[]; declined?: string[] } | null;
};

function List({
  title,
  items,
  tone,
}: {
  title: string;
  items?: string[];
  tone: "good" | "alert" | "ink";
}) {
  if (!items || items.length === 0) return null;
  const color = tone === "good" ? "text-good" : tone === "alert" ? "text-alert" : "text-ink";
  return (
    <div>
      <p className="label-caps mb-1.5">{title}</p>
      <ul className="space-y-1">
        {items.map((it, i) => (
          <li key={i} className="text-[14px] leading-6 flex gap-2">
            <span className={`${color} shrink-0`}>•</span>
            <span>{it}</span>
          </li>
        ))}
      </ul>
    </div>
  );
}

export function ResultAnalysis({ analysis }: { analysis: Analysis }) {
  return (
    <Card className="p-6 space-y-5">
      <div>
        <p className="label-caps mb-1">Your analysis</p>
        {analysis.summary ? (
          <p className="text-[15px] leading-7">{analysis.summary}</p>
        ) : null}
      </div>

      <div className="grid gap-5 sm:grid-cols-2">
        <List title="Strengths" items={analysis.strengths} tone="good" />
        <List title="To work on" items={analysis.weaknesses} tone="alert" />
      </div>

      <List title="Recommendations" items={analysis.recommendations} tone="ink" />

      {analysis.progress ? (
        <div className="border-t border-line pt-5 space-y-4">
          <div>
            <p className="label-caps mb-1">Since your last attempt</p>
            {analysis.progress.summary ? (
              <p className="text-[15px] leading-7">{analysis.progress.summary}</p>
            ) : null}
          </div>
          <div className="grid gap-5 sm:grid-cols-2">
            <List title="Improved" items={analysis.progress.improved} tone="good" />
            <List title="Slipped" items={analysis.progress.declined} tone="alert" />
          </div>
        </div>
      ) : null}

      <p className="text-[11px] text-ink-muted">
        Generated by AI from your scores — a study guide, not an official assessment.
      </p>
    </Card>
  );
}
